    <h1 style="color: #4169E1; font-family: cambria; text-align: center">Seblak Bandung</h1>
    <hr>
    <p style="font-family: gisha; text-align: justify; text-indent: 2em; font-size: medium">Seblak adalah makanan Indonesia yang dikenal berasal dari Bandung, Jawa Barat yang bercita rasa gurih dan pedas. Terbuat dari kerupuk basah yang dimasak dengan sayuran dan sumber protein seperti telur, ayam, boga bahari atau olahan daging sapi, dan dimasak dengan kencur. Seblak kini menjadi makanan jajanan jalanan yang digemari berbagai kalangan masyarakat, terutama di Jawa Barat dan Jabodetabek. Seiring berkembangnya tren jajanan tradisional dan kaki lima, seblak tidak hanya disajikan dengan toping biasa di gerobak. Seblak berkembang menjadi makanan yang modern dan berhasil menarik perhatian. Makanan yang bertekstur kenyal ini memiliki rasa yang pedas dan menyegarkan, serta memiliki beberapa variasi, baik rasa maupun bahan tambahan juga kemasan penjualan.</p>
    <h3 style="font-family: cambria; color:	#DC143C">Bahan-Bahan:</h3>
      <ol style="font-family: gisha; font-size: medium">
        <li>2 genggam kerupuk (redam di air biasa 2 jam).</li>
        <li>6 butir bakso.</li>
        <li>200 g ceker.</li>
      </ol>
    <h3 style="font-family: cambria; color:	#DC143C">Bumbu halus:</h3>
      <ol style="font-family: gisha; font-size: medium">
        <li>4 siung bawang merah.</li>
        <li>3 siung bawang putih.</li>
        <li>3 butir kemiri(boleh di kurangi klo ga suka creamy).</li>
        <li>1 ruas jari kencur.</li>
        <li>8 cabe merah keriting.</li>
        <li>6 cabe rawit pedas.</li>
        <li>5 cabe rawit hijau (untuk irisan).</li>
        <li>Gula, garam, kaldu bubuk, lada bubuk.</li>
        <li>1 bonggol sawi sendok.</li>
        <li>1 batang daun bawang.</li>
        <li>Air kaldubrebusan ceker (sesuai selera).</li>
        <li>2 butir telur yang sudah di orak arik.</li>
      </ol>
    <h3 style="font-family: cambria; color:	#DC143C">Cara memasak :</h3>
      <ol style="font-family: gisha; font-size: medium">
        <li>Tumis bumbu hingga matang.</li>
        <li>Tambahkan bakso, ceker, kerupuk yg sudah di rendam, garam, gula, lada, saus tiram, kaldu bubuk. Masak sebentar.</li>
        <li>Tambah air kaldu, masak hingga mendidih.</li>
        <li>Masukkan daun sawi, daun bawang, dan cabe rawit iris, masak hingga sayuran matang.</li>
      </ol>
