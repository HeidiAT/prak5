    <h1 style="color: skyblue; font-family: cambria; text-align: center">Ayam Lodho Khas Tulungagung</h1>
    <hr>
    <p style="font-family: gisha; text-align: justify; text-indent: 2em; font-size: medium">Ayam lodho merupakan salah satu kuliner khas Tulungagung. Makanan satu ini berupa ayam yang diberi bumbu santan dan biasanya disajikan dengan urap-urap serta nasi gurih. Keunikan olahan ayam khas Jatim ini terletak pada kuah santan yang kental. Rasanya gurih enak dengan aksen pedas cabai plus aroma bakar yang wangi.</p>
    <h3 style="font-family: cambria; color: ">Bahan-Bahan :</h3>
      <ol style="font-family: gisha; font-size: medium">
        <li>1 ekor ayam kampung panggang (ingkung)</li>
        <li>5 siung bawang putih</li>
        <li>8 siung bawang merah</li>
        <li>4 butir kemiri</li>
        <li>1 ruas kunyit</li>
        <li>1 ruas laos</li>
        <li>1/2 ruas jahe</li>
        <li>15-20 cabe rawit</li>
        <li>Secukupnya ketumbar</li>
        <li>Secukupnya garam</li>
        <li>Secukupnya gula</li>
        <li>1 butir meggi block</li>
        <li>2 biji Santan kara</li>
        <li>1 serai</li>
        <li>4 daun jeruk</li>
        <li>Air</li>
      </ol>
    <h3>Cara memasak :</h3>
      <ol style="font-family: gisha; font-size: medium">
        <li>Cuci bersih ayam kampung panggang yang sudah dipotong-potong</li>
        <li>Haluskan bawang merah, bawang putih, kunyit, ketumbar, kemiri, cabe (5butir saja)</li>
        <li>Tumis bumbu yg sudah dihaluskan hingga wangi dan tambahkan serai, laos, jahe yg sudah digeprek, dan tambahkan daun jeruk serta cabe</li>
        <li>Tambahkan air, meggi block tunggu hingga mendidih dan masukkan ayam, *air diusahakan menutupi hingga menutupi ayam</li>
        <li>Tunggu hingga ayam empuk, kira kira hingga air menyusut setengah.</li>
        <li>Tambahkan garam,gula, penyedap dan santan.</li>
        <li>Siap disajikan.</li>
      </ol>
