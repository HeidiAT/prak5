    <h1 style="color: #4169E1; font-family: cambria; text-align: center">Tahu Tek Surabaya</h1>
    <hr>
    <p style="font-family: gisha; text-align: justify; text-indent: 2em; font-size: medium">Tahu tek adalah makanan yang pada dasarnya berisi tahu setengah matang dengan siraman saus kacang. Untuk lebih lengkap, tahu tek berisi tahu setengah matang, kentang goreng, lontong yang disiram dengan saus kacang. Tak lupa untuk tambahan ada yang ditambah dengan kecambah pendek dan yang paling penting kerupuk. Walaupun saus kacangnya terlihat sama dengan saus kacang rujak tapi rasanya berbeda lho. Saus kacang tahu tek lebih sederhana dengan campuran kacang goreng, petis, bawang goreng dan air matang.</p>
    <h3 style="font-family: cambria; color: #CD5C5C">Bahan-Bahan :</h3>
      <ol style="font-family: gisha; font-size: medium">
        <li>Tahu putih</li>
        <li>2 Telor</li>
        <li>2 genggam kacang tanah goreng / -+100gr</li>
        <li>4 siung bw putih</li>
        <li>Sedikit lada</li>
        <li>Garam</li>
        <li>Cabe rawit jika suka pedas</li>
        <li>1/2sdm petis udang</li>
        <li>1-2sdm kecap manis</li>
        <li>Daun seledri iris tipis2</li>
        <li>Kerupuk bawang yg biasanya juga untuk gado2</li>
        <li>2 genggam Toge rebus</li>
        <li>Lontong atau nasi</li>
      </ol>
    <h3 style="font-family: cambria; color: #CD5C5C">Cara memasak :</h3>
      <ol type="a" style="font-family: gisha; font-size: medium">
        <li style="font-weight: bold">Bumbu/saus kacang:</li>
          <ol style="font-family: gisha; font-size: medium">
            <li>Goreng kacang tanah hingga matang, boleh digoreng bersama bawang putih dan cabe rawit, sisihkan.</li>
            <li>Jika sudah dingin blender semuanya ditambah air matang agar blender tidak berat jalannya.</li>
            <li>Tuang di mangkok, tambahkan gula, garam, kecap manis, dan petis udang.</li>
            <li>Aduk rata. Boleh ditambah kecap sampai dapat rasa dan warna saus yg diinginkan.</li>
          </ol>
          <br>
        <li style="font-weight: bold">Tahu telor:</li>
          <ol style="font-family: gisha; font-size: medium">
            <li>Kocok lepas telor.</li>
            <li>Masukkan tahu yg sdh diiris kotak2 kecil tambahkan garam, lada, dan sedikit kaldu bubuk.</li>
            <li>Goreng dengan wajan cekung dengan minyak yang panas dan api cenderung. kecil agar tahu matang sempurna.</li>
          </ol>
          <br>
        <li style="font-weight: bold">Plating:</li>
          <ol style="font-family: gisha; font-size: medium">
            <li>Iris lontong/nasi tata d piring, letakkan tahu telornya.</li>
            <li>Siram sausnya.</li>
            <li>Tambahkan toge dan terakhir irisan daun seledrinya.</li>
            <li>Tambahkan timun jika suka Jangan lupa kerupuknya.</li>
            <li>Sajikan selagi hangat.</li>
          </ol>
      </ol>
