    <h1 style="color: skyblue; font-family: cambria; text-align: center">Pempek Palembang</h1>
    <hr>
    <p style="font-family: gisha; text-align: justify; text-indent: 2em; font-size: medium">Pempek Palembang memang adalah makanan khas Palembang. Kuliner ini terbuat dari ikan giling an tepung kanji. Serta beberapa komposisi lain seperti telur, bawang putih yang dihaluskan, penyedap rasa dan garam. Sebenarnya sulit untuk mengatakan bahwa penganan pempek pusatnya adalah di Palembang karena hampir semua daerah di Sumatera Selatan memproduksinya.</p>
    <h3 style="font-family: cambria; color: #00FF7F">Bahan untuk biang :</h3>
      <ol style="font-family: gisha; font-size: medium">
        <li>400 ml air.</li>
        <li>200 gr terigu.</li>
        <li>Secukupnya Garam dan gula.</li>
      </ol>
    <h3 style="font-family: cambria; color: #00FF7F">Bahan untuk adonan pempek :</h3>
      <ol style="font-family: gisha; font-size: medium">
        <li>3 butir telur dikocok (dicampur ke dalam adonan pempek).</li>
        <li>5 siung bawang putih dihaluskan.</li>
        <li>400 gr sagu tani.</li>
      </ol>
    <h3 style="font-family: cambria; color: #00FF7F">Bahan untuk isian :</h3>
      <ul style="font-family: gisha; font-size: medium">
        <li>2 butir telur dikocok beri garam, gula sedikit.</li>
      </ul>
    <h3 style="font-family: cambria; color: #00FF7F">Cara memasak :</h3>
     <ol style="font-family: gisha; font-size: medium">
       <li>Dalam panci campur terigu, air, garam dan gula. Aduk rata. Nyalakan api, masak sambil diaduk-aduk sampai menggumpal seperti bubur (hati-hati gosong) Angkat. Biarkan hangat.</li>
       <li>Pindahkan biang yang sudah tidak panas ke dalam baskom. Dalam keadaan hangat tambahkan bawang putih, telur kocok. Aduk rata pakai sendok kayu.</li>
       <li>Masukkan sagu sedikit-sedikit. Aduk rata.</li>
       <li>Balur tangan dengan sagu, ambil sebagian adonan bentuk silinder</li>
       <li>Buat lubang dengan menggunakan jari telunjuk. Bentuk seperti contong kemudian isi dengan telur kocok</li>
       <li>Tutup adonan pempek rapat-rapat supaya tidak terbuka pada saat direbus</li>
       <li>Rebus pempek dalam air mendidih sampai matang, angkat.</li>
     </ol>
